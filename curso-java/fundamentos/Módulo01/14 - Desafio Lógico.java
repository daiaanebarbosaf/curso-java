package fundamentos;

public class DesafioLogicos {

	public static void main(String[] args) {
	
		
		// Primeira opção TUDO CERTO
		
		boolean terca = true;
		boolean quinta = false;
		
		System.out.println(terca && quinta);
		System.out.println("Tudo certo, vamos comprar a TV de 50 polegadas e tomar sorvete!");
		
		//em que apenas um dá certo
		System.out.println(terca ^ quinta);
		System.out.println("Poderia ser melhor, mas vamos comprar a TV de 32 polegadas e tomar sorvete!");
		
		// negando as verdadeiras
		System.out.println(terca || !quinta);
		System.out.println("Ops! Não vai rolar sorvete.");
		
		
	}

}
